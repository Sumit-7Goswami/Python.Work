# Write a program to greet all the person names stored in a list L1 and which start with S
#                 L1 = ["Harry" ,"Satyam" , "Sachin" , "Rahul"]

l1 = ["Harry" ,"Satyam" ,"Sachin" , "Rahul"]
for name in l1:
    if name.startswith("S"):
        print("Hello" + name)






















